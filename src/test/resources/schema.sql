CREATE TABLE IF NOT EXISTS refresh_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token_hash VARCHAR(64) NOT NULL,
    usuario_id BIGINT NOT NULL,
    expira_em TIMESTAMP NOT NULL,
    revogado BOOLEAN NOT NULL DEFAULT FALSE,
    criado_em TIMESTAMP NOT NULL,
    CONSTRAINT uk_refresh_tokens_token_hash UNIQUE (token_hash),
    CONSTRAINT fk_refresh_tokens_usuario FOREIGN KEY (usuario_id) REFERENCES usuarios (id)
);

CREATE INDEX IF NOT EXISTS idx_refresh_tokens_usuario_revogado ON refresh_tokens (usuario_id, revogado);
CREATE INDEX IF NOT EXISTS idx_refresh_tokens_expira_em ON refresh_tokens (expira_em);

CREATE TABLE IF NOT EXISTS veiculos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    placa VARCHAR(10) NOT NULL,
    marca VARCHAR(80) NOT NULL,
    modelo VARCHAR(120) NOT NULL,
    ano_fabricacao INTEGER NOT NULL,
    motor VARCHAR(30),
    combustivel VARCHAR(30),
    km_atual INTEGER,
    cliente_id BIGINT NOT NULL,
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT,
    CONSTRAINT uk_veiculos_placa UNIQUE (placa),
    CONSTRAINT fk_veiculos_cliente FOREIGN KEY (cliente_id) REFERENCES clientes (id)
);

CREATE TABLE IF NOT EXISTS ordens_servico (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cliente_id BIGINT NOT NULL,
    veiculo_id BIGINT NOT NULL,
    tecnico_id BIGINT NOT NULL,
    data DATE NOT NULL,
    hora TIME NOT NULL,
    km_entrada INTEGER,
    km_saida INTEGER,
    custo_servicos_terceirizados DECIMAL(12, 2) NOT NULL DEFAULT 0,
    descricao_servicos_terceirizados VARCHAR(500),
    custo_pecas DECIMAL(12, 2) NOT NULL DEFAULT 0,
    custo_mao_de_obra DECIMAL(12, 2) NOT NULL DEFAULT 0,
    descricao_mao_de_obra VARCHAR(500),
    preco_total DECIMAL(12, 2) NOT NULL DEFAULT 0,
    status VARCHAR(30) NOT NULL DEFAULT 'ORCAMENTO',
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT,
    CONSTRAINT fk_ordens_servico_cliente FOREIGN KEY (cliente_id) REFERENCES clientes (id),
    CONSTRAINT fk_ordens_servico_veiculo FOREIGN KEY (veiculo_id) REFERENCES veiculos (id),
    CONSTRAINT fk_ordens_servico_tecnico FOREIGN KEY (tecnico_id) REFERENCES usuarios (id)
);

CREATE TABLE IF NOT EXISTS ordens_servico_itens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ordem_servico_id BIGINT NOT NULL,
    descricao VARCHAR(255) NOT NULL,
    quantidade DECIMAL(10, 2) NOT NULL DEFAULT 1,
    valor_unitario DECIMAL(12, 2) NOT NULL DEFAULT 0,
    tipo VARCHAR(20) NOT NULL,
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT,
    CONSTRAINT fk_ordens_servico_itens_os FOREIGN KEY (ordem_servico_id) REFERENCES ordens_servico (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS ordens_servico_status_historico (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ordem_servico_id BIGINT NOT NULL,
    status_anterior VARCHAR(30),
    status_novo VARCHAR(30) NOT NULL,
    usuario_id BIGINT,
    origem VARCHAR(30) NOT NULL DEFAULT 'SISTEMA',
    observacao VARCHAR(500),
    criado_em TIMESTAMP NOT NULL,
    CONSTRAINT fk_os_status_hist_ordem
        FOREIGN KEY (ordem_servico_id) REFERENCES ordens_servico (id) ON DELETE CASCADE,
    CONSTRAINT fk_os_status_hist_usuario
        FOREIGN KEY (usuario_id) REFERENCES usuarios (id),
    CONSTRAINT chk_os_status_hist_origem
        CHECK (origem IN ('CLIENTE', 'TECNICO', 'ADMIN', 'SISTEMA'))
);

