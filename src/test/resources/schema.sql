CREATE TABLE IF NOT EXISTS enderecos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cep VARCHAR(9) NOT NULL,
    logradouro VARCHAR(255),
    complemento VARCHAR(255),
    numero VARCHAR(20),
    bairro VARCHAR(150),
    cidade VARCHAR(150),
    uf VARCHAR(2),
    estado VARCHAR(100),
    ibge VARCHAR(10),
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT
);

CREATE TABLE IF NOT EXISTS clientes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(150) NOT NULL,
    tipo_pessoa VARCHAR(20) NOT NULL DEFAULT 'PESSOA_FISICA',
    endereco_id BIGINT,
    cpf VARCHAR(14),
    cnpj VARCHAR(18),
    razao_social VARCHAR(150),
    nome_fantasia VARCHAR(150),
    telefone VARCHAR(20),
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT,
    CONSTRAINT uk_clientes_cpf UNIQUE (cpf),
    CONSTRAINT uk_clientes_cnpj UNIQUE (cnpj),
    CONSTRAINT uk_clientes_endereco_id UNIQUE (endereco_id),
    CONSTRAINT fk_clientes_endereco FOREIGN KEY (endereco_id) REFERENCES enderecos (id)
);

CREATE TABLE IF NOT EXISTS usuarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(150) NOT NULL,
    senha VARCHAR(255),
    nome VARCHAR(150) NOT NULL,
    perfil VARCHAR(20) NOT NULL,
    cliente_id BIGINT,
    ativo BOOLEAN NOT NULL DEFAULT TRUE,
    deve_definir_senha BOOLEAN NOT NULL DEFAULT FALSE,
    criado_em TIMESTAMP NOT NULL,
    atualizado_em TIMESTAMP NOT NULL,
    usuario_inclusao_id BIGINT,
    usuario_alteracao_id BIGINT,
    CONSTRAINT uk_usuarios_email UNIQUE (email),
    CONSTRAINT uk_usuarios_cliente_id UNIQUE (cliente_id),
    CONSTRAINT fk_usuarios_cliente FOREIGN KEY (cliente_id) REFERENCES clientes (id)
);

